int countPointsInInterval(const std::vector<double>& array, 
    double lowerBound, double upperBound) {
    int count = 0;
    for (const double& value : array) {
        if (value >= lowerBound && value <= upperBound) {
            count++;
        }
    }
    return count;
}
long double fact(int N)
{
    if (N < 0) 
        return 0; 
    if (N == 0)
        return 1; 
    else 
        return N * fact(N - 1); 
}
